#patterns shape concept
#right angle triangle
x=int(input("enter the number: "))
for i in range(1,x+1):
    print("*"  *i)
    
#left angle traingle in patterns
x=int(input("enter the number of rows: "))
for i in range(1,x+1):
    print(" "*(x-i)+"*" *i)

